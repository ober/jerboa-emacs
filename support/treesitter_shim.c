/*
 * treesitter_shim.c — Tree-sitter integration for Chez Scheme FFI.
 *
 * Wraps the tree-sitter C API with flat functions suitable for Chez
 * foreign-procedure calls.  TSNode (24-byte struct) is handled via
 * static node slots since Chez FFI cannot pass structs by value.
 *
 * Compile (dynamic):
 *   gcc -shared -fPIC -O2 -o treesitter_shim.so treesitter_shim.c \
 *       -ltree-sitter -Wall
 *
 * For static linking the grammar language functions (tree_sitter_c, etc.)
 * are resolved at link time from per-language .a archives.
 */

#include <tree_sitter/api.h>
#include <string.h>
#include <stdlib.h>

