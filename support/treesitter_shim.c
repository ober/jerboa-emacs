const char *ts_shim_get_highlight_query(const char *lang) {
    const char *q = ts_embedded_query_get(lang);
    return q ? q : "";
}

int ts_shim_get_highlight_query_len(const char *lang) {
    return ts_embedded_query_len(lang);
}
